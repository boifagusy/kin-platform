<script>
    const menuBtn = document.getElementById('menuButton');
    const mobileSidebar = document.getElementById('mobileSidebar');
    const mobileOverlay = document.getElementById('mobileOverlay');
    if (menuBtn && mobileSidebar && mobileOverlay) {
        function openSidebar() { mobileSidebar.classList.remove('-translate-x-full'); mobileOverlay.classList.remove('hidden'); document.body.style.overflow = 'hidden'; }
        function closeSidebar() { mobileSidebar.classList.add('-translate-x-full'); mobileOverlay.classList.add('hidden'); document.body.style.overflow = ''; }
        menuBtn.addEventListener('click', openSidebar);
        mobileOverlay.addEventListener('click', closeSidebar);
        document.addEventListener('keydown', function(e) { if (e.key === 'Escape' && !mobileSidebar.classList.contains('-translate-x-full')) { closeSidebar(); } });
    }
</script>


